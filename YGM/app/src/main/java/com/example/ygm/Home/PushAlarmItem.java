package com.example.ygm.Home;

public class PushAlarmItem {

    String title;
    String date;

    public PushAlarmItem(String title, String date) {
        this.title = title;
        this.date = date;
    }

    public String getTitle() {
        return title;
    }

    public String getDate() {
        return date;
    }
}
